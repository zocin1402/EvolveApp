CREATE TABLE Business (
	[Index] INT NOT NULL IDENTITY(1, 1),
	Code VARCHAR(200) NOT NULL DEFAULT '',
	[Name] NVARCHAR(200) NOT NULL DEFAULT '',
	IsDelete BIT,
	CreateUser VARCHAR(200) NOT NULL DEFAULT '',
	CreatedDate DATETIME2,
	ModifiedUser NVARCHAR(200) NOT NULL DEFAULT '',
	ModifiedDate DATETIME2,
	DeletedUser NVARCHAR(200) NOT NULL DEFAULT '',
	DeletedDate DATETIME2,
	PRIMARY KEY ([Index])
)